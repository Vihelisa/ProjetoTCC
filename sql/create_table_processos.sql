CREATE TABLE arquivos_processos (
    id_arquivo NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    numero_processo VARCHAR2(50),
    nome_arquivo VARCHAR2(255),
    arquivo_pdf BLOB,
    CONSTRAINT fk_processo
        FOREIGN KEY (numero_processo)
        REFERENCES processos_juridicos (numero_processo)
        ON DELETE CASCADE
);