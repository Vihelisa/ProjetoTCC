-- Criar tabela dos dados do processo jurídico
CREATE TABLE processos_juridicos (
    numero_processo VARCHAR2(50) PRIMARY KEY,
    classe_processo VARCHAR2(100),
    rito_processo VARCHAR2(100),
    nome_advogado VARCHAR2(255),
    numero_oab VARCHAR2(50),
    nome_cliente_empresa VARCHAR2(255),
    caminho_processual VARCHAR2(100),
    nome_juiz VARCHAR2(255),
    estado_processo CHAR(2),
    valor_causa NUMBER,
    valor_deferido_causa NUMBER,
    valor_pago_causa NUMBER,
    observacoes_clob CLOB
);


